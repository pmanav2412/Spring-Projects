package com.demo.model;

public class Student {
	private String name;
	private long lastname;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public long getLastname() {
		return lastname;
	}

	public void setLastname(long lastname) {
		this.lastname = lastname;
	}

}
